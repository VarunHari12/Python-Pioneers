# What is a function?

# a function is a peice of code that can be used by a programmer to complete small customized tasks 
# you can write your own functions (we will cover this later in the course)
# you can also use functions from other libraries
# libraries are programs that other people have written that can be imported into your own program
# you can use certain functionalities of a library by using a . and then specifying which function you want to use
# the syntax for importing a entire library is import (library)
# for importing specific functions in a library the syntax is from (library) import (function), (function) etc.
# to use a function from another library, the syntax is (library).(function)
# using a function is called calling a function